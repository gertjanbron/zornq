# ============================================================
# 3. Max-k-Cut bounds en reductie naar MaxCut
# ============================================================

def max_k_cut_upper_bound(graph: WeightedGraph, k: int) -> float:
    """Triviale UB: Max-k-Cut ≤ m (alle edges gesnijd als χ(G) ≤ k).

    Preciezer: voor ongewogen graaf geeft een random k-partitie verwachting
    m · (1 − 1/k). Frieze-Jerrum geeft α_k · m met α_k → 1 als k → ∞:
        α_2 ≈ 0.87856, α_3 ≈ 0.83276, α_4 ≈ 0.85022, α_5 ≈ 0.87693.

    Als χ(G) ≤ k, dan is Max-k-Cut = totaal edge-gewicht (alle edges gesnijd).
    """
    return float(graph.total_weight())


def frieze_jerrum_lower_bound(graph: WeightedGraph, k: int) -> float:
    """Frieze-Jerrum gegarandeerde approximatie ondergrens voor Max-k-Cut."""
    alpha_table = {2: 0.87856, 3: 0.83276, 4: 0.85022, 5: 0.87693}
    alpha = alpha_table.get(k, 1.0 - 1.0 / k)  # val terug op random-bound
    return alpha * graph.total_weight()


def dimacs_to_maxcut(graph: WeightedGraph) -> WeightedGraph:
    """Trivial identity: DIMACS unweighted edge-graph = MaxCut input met w=1.

    (De ware Max-k-Cut → MaxCut reductie via de k-expansion-gadget is polynoom
    maar vergroot |V| met factor k. Voor k=2 is het gewoon de identiteit.)
    """
    g_out = WeightedGraph()
    for v in graph.nodes:
        g_out.add_node(v)
    for i, j, w in graph.edges():
        g_out.add_edge(i, j, w)
    return g_out


