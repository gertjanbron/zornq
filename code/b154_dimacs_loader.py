def load_fixture(name: str) -> tuple[WeightedGraph, int, int]:
    """Laad een ingebouwde kleine DIMACS-graaf."""
    if name not in DIMACS_FIXTURES:
        raise ValueError(f"Onbekende fixture: {name}. Beschikbaar: {list(DIMACS_FIXTURES)}")
    return parse_dimacs(DIMACS_FIXTURES[name], from_text=True)


# ============================================================
# 5. CLI
# ============================================================

def _main() -> None:
    ap = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument("--list", action="store_true", help="Toon ingebouwde fixtures + DIMACS-DB")
    ap.add_argument("--file", type=str, default=None, help="Laad DIMACS-file")
    ap.add_argument("--fixture", type=str, default=None, help="Laad ingebouwde fixture (petersen, k4, ...)")
    ap.add_argument("--k", type=int, default=2, help="k voor Max-k-Cut UB (default 2)")
    ap.add_argument("--write", type=str, default=None, help="Schrijf DIMACS-output")
    args = ap.parse_args()

    if args.list:
        print("DIMACS fixtures (inline):")
        for name in DIMACS_FIXTURES:
            print(f"  {name}")
        print()
        print(f"DIMACS chromatic-DB: {len(DIMACS_CHROMATIC)} instanties")
        for name, (n, m, chi) in sorted(DIMACS_CHROMATIC.items()):
            print(f"  {name:<15s} n={n:4d}  m={m:5d}  χ={chi}")
        return

    g = None
    name = ""
    if args.file:
        g, n_decl, m_decl = parse_dimacs(args.file)
        name = os.path.splitext(os.path.basename(args.file))[0]
    elif args.fixture:
        g, n_decl, m_decl = load_fixture(args.fixture)
        name = args.fixture
    else:
        ap.print_help()
        return

    print(f"DIMACS graph: {name}")
    print(f"  declared: n={n_decl}, m={m_decl}")
    print(f"  parsed:   n={g.n_nodes}, m={g.n_edges}, total_w={g.total_weight():g}")

    ub = max_k_cut_upper_bound(g, args.k)
    lb = frieze_jerrum_lower_bound(g, args.k)
    print(f"  Max-{args.k}-Cut UB (= total weight, tight als χ≤{args.k}): {ub:g}")
    print(f"  Frieze-Jerrum LB (α_{args.k} · m): {lb:.2f}")

    if name in DIMACS_CHROMATIC:
        _, _, chi = DIMACS_CHROMATIC[name]
        print(f"  χ(G) = {chi}  (dus Max-{chi}-Cut = m = {g.n_edges})")

    if args.write:
        write_dimacs(g, args.write)
        print(f"  written to: {args.write}")


if __name__ == "__main__":
    _main()
