# ============================================================
# 1. DIMACS parser
# ============================================================

def parse_dimacs(source: str, from_text: bool = False) -> tuple[WeightedGraph, int, int]:
    """Parse DIMACS edge-format.

    Lines:
      c comment
      p edge N M
      e i j           (1-indexed vertices)
      e i j w         (met edge-gewicht; niet standaard maar we accepteren het)

    Returns:
      (WeightedGraph, declared_n, declared_m)
    """
    if from_text:
        lines = source.strip().splitlines()
    else:
        with open(source) as f:
            lines = f.read().strip().splitlines()

    n_declared = 0
    m_declared = 0
    g = WeightedGraph()
    nodes_initialized = False

    for line in lines:
        line = line.strip()
        if not line or line.startswith("c"):
            continue
        parts = line.split()
        if parts[0] == "p":
            # "p edge N M"  of "p col N M"
            if len(parts) < 4:
                continue
            n_declared = int(parts[2])
            m_declared = int(parts[3])
            for i in range(n_declared):
                g.add_node(i)
            nodes_initialized = True
        elif parts[0] == "e":
            if not nodes_initialized:
                raise ValueError("DIMACS: 'e' line vóór 'p' header")
            i = int(parts[1]) - 1
            j = int(parts[2]) - 1
            w = float(parts[3]) if len(parts) >= 4 else 1.0
            if i == j:
                continue
            g.add_edge(i, j, w)
        # else: skip (bv. 'n <vertex> <label>' voor node-labels)

    return g, n_declared, m_declared


def write_dimacs(graph: WeightedGraph, filepath: str, weighted: bool = False) -> None:
    """Schrijf graaf als DIMACS edge-format."""
    edges = list(graph.edges())
    with open(filepath, "w") as f:
        f.write(f"c DIMACS edge-format (generated by b154_dimacs_loader)\n")
        f.write(f"p edge {graph.n_nodes} {len(edges)}\n")
        for i, j, w in edges:
            if weighted:
                f.write(f"e {i+1} {j+1} {w:g}\n")
            else:
                f.write(f"e {i+1} {j+1}\n")


